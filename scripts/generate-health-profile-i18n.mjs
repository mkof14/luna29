const q = (s) => String(s).replace(/\\/g, '\\\\').replace(/'/g, "\\'");

const emitValue = (v, indent) => {
  const pad = '  '.repeat(indent);
  if (Array.isArray(v)) {
    return `[${v.map((x) => `'${q(x)}'`).join(', ')}]`;
  }
  if (v && typeof v === 'object') {
    const lines = Object.entries(v).map(([k, val]) => `${pad}  ${k}: '${q(val)}',`);
    return `{\n${lines.join('\n')}\n${pad}}`;
  }
  return `'${q(v)}'`;
};

const emitLang = (lang, data) => {
  const keys = Object.keys(en);
  const lines = keys.map((k) => {
    const v = data[k];
    if (k === 'entryLiveBySection' || k === 'badgeSources' || k === 'missingLabels' || k === 'sections') {
      return `    ${k}: ${emitValue(v, 2)} as Record<string, string>,`;
    }
    if (Array.isArray(v)) {
      return `    ${k}: ${emitValue(v, 2)},`;
    }
    return `    ${k}: ${emitValue(v, 2)},`;
  });
  return `  ${lang}: {\n${lines.join('\n')}\n  }`;
};

